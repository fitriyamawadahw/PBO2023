    private void jButtonsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsimpanActionPerformed
        // TODO add your handling code here:
        String kodeProdi = jTextFieldkode.getText().trim();
        String namaProdi = jTextFieldnamaprodi.getText();
        String jenjang = jTextFieldjenjang.getText();
        String namaKaprodi = jTextFieldnamakaprodi.getText();
        if (!kodeProdi.isEmpty() && !namaProdi.isEmpty() && !jenjang.isEmpty() && !namaKaprodi.isEmpty()) {

            try {
                // TODO add your handling code here:
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5433/TugasPBO", "postgres", "pipit1702");
                int k = masukkanData(conn, kodeProdi, namaProdi, jenjang, namaKaprodi);
                if (k > 0) {
                    tampil(conn);
                    this.peringatan("Simpan Berhasil");
                } else {
                    this.peringatan("Simpan Gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.peringatan("Input Data Mahasiswa Gagal");
        }
        jTextFieldkode.setText("");
        jTextFieldnamaprodi.setText("");
        jTextFieldjenjang.setText("");
        jTextFieldnamakaprodi.setText("");
        kosongkan_form();
    }//GEN-LAST:event_jButtonsimpanActionPerformed

