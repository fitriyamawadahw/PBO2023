    private int masukkanData(Connection conn, String kodeProdi, String namaProdi, String jenjang, String namaKaprodi) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("INSERT INTO program_studi (kode_prodi,nama_prodi,jenjang,nama_kaprodi) VALUES(?,?,?,?)");
        pst.setString(1, kodeProdi);
        pst.setString(2, namaProdi);
        pst.setString(3, jenjang);
        pst.setString(4, namaKaprodi);
        return pst.executeUpdate();
    }

    private int updateData(Connection conn, String kodeProdi, String namaProdi, String jenjang, String namaKaprodi) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("UPDATE program_studi SET nama_prodi=?, jenjang=?, nama_kaprodi=? WHERE kode_prodi=?");
        pst.setString(1, namaProdi);
        pst.setString(2, jenjang);
        pst.setString(3, namaKaprodi);
        pst.setString(4, kodeProdi);
        return pst.executeUpdate();
    }

    private int deleteData(Connection conn, String kodeProdi) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("DELETE FROM program_studi WHERE kode_prodi=?");
        pst.setString(1, kodeProdi);
        return pst.executeUpdate();
    }

    private void tampil(Connection conn) {
        dataProdi.clear();
        try {
            String sql = "select * from program_studi";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                ProgramStudi data = new ProgramStudi();
                data.setKodeProdi(String.valueOf(rs.getObject(1)));
                data.setNamaProdi(String.valueOf(rs.getObject(2)));
                data.setJenjang(String.valueOf(rs.getObject(3)));
                data.setNamaKaprodi(String.valueOf(rs.getObject(4)));
                dataProdi.add(data);
            }
            DefaultTableModel model = (DefaultTableModel) jTableProdi.getModel();
            model.setRowCount(0);
            for (ProgramStudi data : dataProdi) {

                Object[] baris = new Object[4];
                baris[0] = data.getKodeProdi();
                baris[1] = data.getNamaProdi();
                baris[2] = data.getJenjang();
                baris[3] = data.getNamaKaprodi();
                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

